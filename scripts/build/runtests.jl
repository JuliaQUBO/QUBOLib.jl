function _write_qoblib_fixture_group(root::AbstractString, group, filenames)
    group_path = mkpath(joinpath(root, group.path))

    write(joinpath(group_path, "README.md"), "# Fixture\n")
    write(
        joinpath(group_path, "metrics.csv"),
        join(
            vcat(
                ["file,num_variables,density,min_coeff,max_coeff"],
                ["$(basename(filename)),3,$(4 / 6),-4.0,3.0" for filename in filenames],
            ),
            "\n",
        ) * "\n",
    )

    for filename in filenames
        model_path = joinpath(group_path, filename)

        mkpath(dirname(model_path))
        write(model_path, _qoblib_fixture())
    end

    return nothing
end

function _write_qoblib_solution_dir(
    root::AbstractString,
    group,
    readme::AbstractString = "# Fixture\n",
)
    solution_path = mkpath(joinpath(root, group.solution_path))

    write(joinpath(solution_path, "README.md"), readme)

    return solution_path
end

function _write_qoblib_submission_summary(
    root::AbstractString,
    path::AbstractString,
    problem::AbstractString;
    submitter::AbstractString,
    date::AbstractString,
    best = "0.0",
    bound = "0.0",
    algorithm_type::AbstractString = "Deterministic",
    feasible_runs = "1",
    successful_runs = "1",
    remarks::AbstractString = "fixture submission",
)
    submission_path = mkpath(joinpath(root, path, problem))
    header = join(
        [
            "Problem",
            "Submitter",
            "Date",
            "Reference",
            "Best Objective Value",
            "Optimality Bound",
            "Modeling Approach",
            "# Decision Variables",
            "# Binary Variables",
            "# Integer Variables",
            "# Continuous Variables",
            "# Non-Zero Coefficients",
            "Coefficients Type",
            "Coefficients Range",
            "Workflow",
            "Algorithm Type",
            "# Runs",
            "# Feasible Runs",
            "# Successful Runs",
            "Success Threshold",
            "Hardware Specifications",
            "Total Runtime",
            "CPU Runtime",
            "GPU Runtime",
            "QPU Runtime",
            "Other HW Runtime",
            "Remarks",
        ],
        ",",
    )
    row = join(
        [
            problem,
            submitter,
            date,
            "fixture reference",
            string(best),
            string(bound),
            "QUBO",
            "3",
            "3",
            "0",
            "0",
            "4",
            "Integer",
            "1 - 3",
            "fixture workflow",
            algorithm_type,
            "1",
            string(feasible_runs),
            string(successful_runs),
            "0.0",
            "fixture hardware",
            "1.5",
            "1.0",
            "N/A",
            "N/A",
            "N/A",
            remarks,
        ],
        ",",
    )

    write(joinpath(submission_path, "$(problem)_summary.csv"), "$header\n$row\n")

    return submission_path
end

function _write_qoblib_portfolio_solution(root::AbstractString, group)
    solution_path = _write_qoblib_solution_dir(
        root,
        group,
        """
        |   a |   t | s    |   b |   l | Best |
        |-----|-----|------|-----|-----|------|
        |  10 |  10 | orig |   4 |   0 |  999 |
        """,
    )
    archive_path = joinpath(solution_path, "a010_t10_orig_b004.tar.gz")

    mktempdir() do path
        member_dir = mkpath(joinpath(path, "a010_t10_orig_b004"))

        write(
            joinpath(member_dir, "a010_t10_orig_b004_l0.0.sol"),
            """
            x#1 0
            x#2 1
            x#3 0
            """,
        )
        run(Cmd(["tar", "-czf", archive_path, "-C", path, "a010_t10_orig_b004"]))
    end

    return nothing
end

